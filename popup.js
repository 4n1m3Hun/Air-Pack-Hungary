const modal = document.getElementById("productModal");
const modalImage = document.getElementById("modalImage");
const modalCategory = document.getElementById("modalCategory");
const modalTitle = document.getElementById("modalTitle");
const modalDescription = document.getElementById("modalDescription");
const modalUses = document.getElementById("modalUses");
const modalBenefits = document.getElementById("modalBenefits");
const closeButton = document.querySelector(".close-modal");
const popleft = document.getElementById("popleft");
const popright = document.getElementById("popright");
document.querySelectorAll(".details-btn").forEach(button => {
    button.addEventListener("click", () => {
        const product = products[button.dataset.product];
        modalImage.src = product.image;
        modalImage.alt = product.title;
        modalCategory.textContent = product.category;
        modalTitle.textContent = product.title;
        modalDescription.textContent = product.description;
        modalUses.innerHTML = "";
        const list = product.specifications || product.uses || [];
        list.forEach(item => {
            modalUses.innerHTML += `<li>${item}</li>`;
        });
        modalBenefits.innerHTML = "";
        product.benefits.forEach(item => {
            modalBenefits.innerHTML += `<li>${item}</li>`;
        });
        modal.classList.add("active");
        document.body.style.overflow = "hidden";
        popleft.innerHTML = product.left;
        popright.innerHTML = product.right;
    });
});
closeButton.addEventListener("click", closeModal);
modal.addEventListener("click", (event) => {
    if (event.target === modal) {
        closeModal();
    }
});
document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") {
        closeModal();
    }
});
function closeModal() {
    modal.classList.remove("active");
    document.body.style.overflow = "auto";
}